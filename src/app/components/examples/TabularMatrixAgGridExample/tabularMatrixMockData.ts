const sum = (rows: TabularMatrixRow[], key: keyof TabularMatrixRow): number | null => {
  let total = 0
  let any = false
  for (const r of rows) {
    const v = r[key]
    if (typeof v === 'number') {
      total += v
      any = true
    }
  }
  return any ? total : null
}

